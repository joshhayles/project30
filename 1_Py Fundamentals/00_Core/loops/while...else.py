
# use while...else to run a block of code once the condition is no longer true
# i = 20
# while i < 15:
#     print(i)
#     i += 1
# else:
#     print("i is no longer less than 15") # => i is no longer less than 15

i = 20
while i < 21:
    print(i)
    i += 1
else:
    print("i is no longer less than 21")
# => 20
# => i is no longer less than 21