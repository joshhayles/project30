# used to specify a type onto a variable

# integer casting
x = int(1) # x will be 1
y = int(2.8) # y will be 2
print(y)
z = int("3") # x will be 3
print(z)

# float casting
x = float(1) # x will be 1.0
y = float(2.8) # y will be 2.8
z = float("3") # z will be 3.0
w = float("4.2") # w will be 4.2
print(w)

# string casting
x2 = str("s1") # x2 will be 's1'
print(x2)
x3 = str(2) # x3 will be '2'
print(x3)
x4 = str(3.0) # x4 will be '3.0'
print(x4)
