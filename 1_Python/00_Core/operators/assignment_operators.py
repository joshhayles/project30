
# used to assign values to variables

x = 5
x += 3 # same as x = x + 3
x -= 3 # same as x = x - 3
x *= 3 # same as x = x * 3
x /= 3 # same as x = x / 3
x %= 3 # same as x = x % 3

# floor division
x //= 3 # same as x = x // 3 | rounds down to nearest whole number
x = 5
x //= 3
print(x) # => 1

# exponentiation
x **= 3 # same as x = x ** 3
x = 5
x **= 3
print(x) # => 125

# bitwise AND operation
x = 5
x &= 3
print(x) # => 1

# bitwise OR operation
x = 5
x |= 3
print(x) # => 7

